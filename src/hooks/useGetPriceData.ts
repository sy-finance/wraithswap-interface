const WRAFTMLP = '0x2e9aC7f69e3c747420C918954f823a8e98AccAe2'
const WRAToken = '0x4CF098d3775Bd78a4508a13E126798Da5911b6cd'
const WFTM = '0x21be370d5312f44cb42ce377bc9b8a0cef1a4c83'

const web3 = new Web3('https://rpc.ftm.tools/') //No WSS required

let wraithApi = 'https://api.binance.com/api/v3/ticker/price?symbol=FTMUSDT'

const wraithLPContract = new web3.eth.Contract(ERC20ABI, WRAFTMLP)
const wraithContract = new web3.eth.Contract(ERC20ABI, WRAToken)
const wFTMContract = new web3.eth.Contract(ERC20ABI, WFTM)

const useGetPriceData = () => {
  const [data, setData] = useState<number>(0)

  useEffect(() => {
    const fetchData = async () => {
      try {
        setInterval(async function () {
          let result = await axios(wraithApi)

          // let totalSupply = await wraithContract.methods.totalSupply().call();

          let totalFTMperWRA = await wFTMContract.methods.balanceOf(WRAFTMLP).call()

          let totalWRAinLP = await wraithContract.methods.balanceOf(WRAFTMLP).call()

          let WRAprice = (totalFTMperWRA / totalWRAinLP) * result.data.price

          setData(WRAprice)
        }, 3000)
      } catch (error) {
        console.error('Unable to fetch price data:', error)
      }
    }

    fetchData()
  }, [wraithApi])

  return data
}

export default useGetPriceData
